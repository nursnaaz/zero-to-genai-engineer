import { useState } from "react";
import Container from "@cloudscape-design/components/container";
import Header from "@cloudscape-design/components/header";
import Button from "@cloudscape-design/components/button";
import Alert from "@cloudscape-design/components/alert";
import Textarea from "@cloudscape-design/components/textarea";

interface Props {
  studentName: string;
  sessionTitle: string;
  mcqPct: number;
  voiceScore: number;
  topics: string[];
}

/** Produce a WhatsApp-friendly plain-text report with bold markdown (*text*). */
function formatWhatsApp(props: Props): string {
  const { studentName, sessionTitle, mcqPct, voiceScore, topics } = props;

  const verdict =
    voiceScore >= 4.2
      ? "Strong — Interview Ready ✅"
      : voiceScore >= 3.0
      ? "Good — Minor Gaps 📈"
      : voiceScore >= 2.0
      ? "Developing — Keep Practising 🔄"
      : "Foundational — Revisit Core Concepts 📚";

  return [
    `*Distill Assessment Report*`,
    `Student: ${studentName}`,
    `Session: ${sessionTitle}`,
    ``,
    `📊 *Results*`,
    `• MCQ Score: ${mcqPct}%`,
    `• AI Interview Score: ${voiceScore.toFixed(1)}/5.0`,
    `• Verdict: ${verdict}`,
    ``,
    `📚 *Topics Covered*`,
    topics.map((t) => `• ${t}`).join("\n"),
    ``,
    `_Generated by Distill · Inceptez_`,
  ].join("\n");
}

export default function WhatsAppExport(props: Props) {
  const [copied, setCopied] = useState(false);
  const message = formatWhatsApp(props);

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(message);
      setCopied(true);
      // Auto-dismiss the success banner after 3 seconds
      setTimeout(() => setCopied(false), 3000);
    } catch {
      // Clipboard API unavailable (e.g. non-HTTPS) — user can select text manually
    }
  };

  return (
    <Container
      header={
        <Header
          variant="h2"
          description="Share your results via WhatsApp"
          actions={
            <Button variant="primary" iconName="copy" onClick={handleCopy}>
              {copied ? "Copied!" : "Copy to WhatsApp"}
            </Button>
          }
        >
          Share Results
        </Header>
      }
    >
      {copied && (
        <Alert
          type="success"
          dismissible
          onDismiss={() => setCopied(false)}
        >
          Copied! Paste into WhatsApp to share your results.
        </Alert>
      )}
      <Textarea value={message} readOnly rows={12} />
    </Container>
  );
}
